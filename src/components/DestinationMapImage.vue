    },
    zoom: {
      type: Boolean,
      required: true
    }
  },
  computed: {
    sectionPadding(){
      const height = window.innerHeight;
      if (height > 800) {
        return height / 4;
      } else if (height < 800 && height >= 720) {
        return height / 3;
      } else if (height < 720 && height >= 500) {
        return height / 2;
      }
    },
    svgStyle() {
      if (this.zoom) {
        switch(this.activeDestination.id) {
          case 'bled':
